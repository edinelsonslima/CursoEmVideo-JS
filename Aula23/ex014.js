var c = 0

console.log(`\n-------WHILE-------`)
while (c<=4) {
    console.log(`Repetição "While": ${c}`)
    c++
}

console.log(`\n-------DO-WHILE-------`)

do {
    console.log(`Repetição "Do While": ${c}`)
    c++
} while (c <= 9);

console.log(`\n-------FOR-------`)

for (c; c<=14; c++){
    console.log(`Repetição "For": ${c}`)
}